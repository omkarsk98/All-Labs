void rotatePoint(int *x_cordinate, int *y_cordinate, int x_pivot, int y_pivot, int angle)
{
  *x_cordinate = *x_cordinate - x_pivot;
  *y_cordinate = *y_cordinate - y_pivot;
  *x_cordinate = *x_cordinate * cos(angle) - *y_cordinate * sin(angle);
  *y_cordinate = *x_cordinate * sin(angle) + *y_cordinate * cos(angle);
  *x_cordinate += x_pivot;
  *y_cordinate += y_pivot;
}

